package cn.fcars.infomgr.common;

public class TimeProcess {
    private String tjTime;
    private String qrTime;
    private String ckTime;
    private String fsTime;
    private String fkTime;

    public String getTjTime() {
        return tjTime;
    }

    public void setTjTime(String tjTime) {
        this.tjTime = tjTime;
    }

    public String getQrTime() {
        return qrTime;
    }

    public void setQrTime(String qrTime) {
        this.qrTime = qrTime;
    }

    public String getCkTime() {
        return ckTime;
    }

    public void setCkTime(String ckTime) {
        this.ckTime = ckTime;
    }

    public String getFsTime() {
        return fsTime;
    }

    public void setFsTime(String fsTime) {
        this.fsTime = fsTime;
    }

    public String getFkTime() {
        return fkTime;
    }

    public void setFkTime(String fkTime) {
        this.fkTime = fkTime;
    }
}
